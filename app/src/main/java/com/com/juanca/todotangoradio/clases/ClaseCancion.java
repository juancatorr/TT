public class ClaseCancion {

    private String id;
    private String titulo;
    private String descripcion;
    private String ruta;
    private String musica;
    private String letra;
    private String canta;
    private String orquesta;



    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public String getMusica() {
        return musica;
    }

    public void setMusica(String musica) {
        this.musica = musica;
    }

    public String getLetra() {
        return letra;
    }

    public void setLetra(String letra) {
        this.letra = letra;
    }

    public String getCanta() {
        return canta;
    }

    public void setCanta(String canta) {
        this.canta = canta;
    }

    public String getOrquesta() {
        return orquesta;
    }

    public void setOrquesta(String orquesta) {
        this.orquesta = orquesta;
    }
}
